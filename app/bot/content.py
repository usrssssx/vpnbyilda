def _value(value: str, fallback: str) -> str:
    return escape(value.strip()) if value and value.strip() else escape(fallback)


def _support_contact() -> str:
    return _value(app_settings.SERVICE_SUPPORT_CONTACT, app_settings.VPN_HELP_ACCOUNT or "[EMAIL / @USERNAME]")


def _site_url() -> str:
    return _value(app_settings.LEGAL_SITE_URL, app_settings.web_app_url)


def _offer_url() -> str:
    return _value(app_settings.LEGAL_OFFER_URL, f"{app_settings.web_app_url}/offer")


