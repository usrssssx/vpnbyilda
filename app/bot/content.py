def tariffs_caption(prices: list[tuple[int, float]] | None = None) -> str:
    lines = [
        f"<b>Тарифы {escape(app_settings.SERVICE_NAME)}</b>",
        "",
        "Подписка оформляется в приложении Telegram Mini App, а бот помогает быстро разобраться в условиях.",
        "",
        "<b>Что входит в каждый тариф:</b>",
        "• защищённое соединение;",
        "• выдача конфигурации после активации;",
        f"• подключение до {_value(app_settings.SERVICE_MAX_DEVICES, '[N]')} устройств в зависимости от тарифа;",
        "• инструкция по настройке;",
        "• техническая поддержка.",
        "",
    ]
    if prices:
        lines.append("<b>Актуальные планы:</b>")
        for days, price in prices:
            lines.append(f"• {days} дней — от {int(price)} ₽")
        lines.append("")

    lines.extend(
        [
            "Для покупки откройте приложение и выберите подходящий срок подписки.",
            "",
            "<b>Важно:</b> оплата и управление доступом выполняются в приложении, где уже подключен рабочий сценарий покупки.",
        ]
    )
    return "\n".join(lines)


